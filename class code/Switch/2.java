class switchDemo {
    public static void main(String args[]){
        int x=5;
        switch(x){
            case 1:
                    System.out.println("1");
                    break;
            case 2:
                    System.out.println("2");
            case 3:
                    System.out.println("3");
                    break;
            case 5:
                System.out.println("first-5");
                break;
            case 5:
                System.out.println("second-5");
                break;
        }
    }
    
}
